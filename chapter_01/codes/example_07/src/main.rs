fn main() {
    println!("Una simple division de numeros enteros 1/2: {}", 1/2);
    println!("Una division de numeros flotantes 1.0/2.0: {}", 1.0/2.0);
    //print!("{}", 1.0/2);
    //print!("{}", 1/2.0);
    println!("Suma de dos numeros flotantes 10.3 + 10.8: {}", 10.3 + 10.8);
    println!("Suma de dos numeros flotantes 10.3 + 10.9: {}", 10.3 + 10.9); 
    //print!("{}", 10.3 + 1);
    //print!("{}", 10.3 * 1);
    println!("Suma de flotante con conversion de entero explicita 10.3 + 1. : {}", 10.3 + 1.);
    println!("Producto de flotante con conversion de entero explicita 10.3 * 1. : {}", 10.3 * 1.);
    println!("10%3: {}", 10%3);
    println!("-10%3: {}", -10%3);
    println!("10%(-3): {}", 10%(-3));
    println!("-10%(-3): {}", -10%(-3));
    println!("10.%3.: {}", 10.%3.);
    println!("-10.%3.: {}", -10.%3.);
    println!("10.1%3.: {}", 10.1%3.);
}



